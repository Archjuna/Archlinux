function banner() 
{
	line 
	NB_CARACT_2=$((`echo $1 | wc -c`/2))
	PLACE=$(($MIDDLE_ROWS-$NB_CARACT_2))
	IN_MIDDLE="\033["$PLACE"C" 
    	echo -e "\r$IN_MIDDLE$COL_GREEN $1$COL_RESET "
}

function speak()
{
	line
	echo -e "\n\n $1\n"
	line
}

function action()
{
	echo -ne "$COL_YELLOW==> $1...$COL_RESET"
}

function ask()
{
	line
	echo -e "\n"
	read -p $'\e[34m'"$1 "$'\e[0m' answer
}


function ok() 
{
	echo -e "\r$TO_RIGHT$COL_GREEN[ok]$COL_RESET"
}

function error() 
{
	echo -e "\r$TO_RIGHT_ERR$COL_RED[error]$COL_RESET" 
}

function line()
{
	i=1
	while [ $i -le $NB_ROWS ]
	do
		echo -n "#"
		i=$(( $i + 1))		
	done
}
